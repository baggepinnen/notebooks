# ╔═╡ 0566a669-00ca-46a7-a987-cf2131cd7cc8
md"""
## Feedback-gain parametrization
For completeness, lets also parameterize the observer-based state-feedback controller using the gain matrices directly, that is, we search directly over ``L`` and ``K`` in the controller
```math
C_{fb} = \begin{bmatrix} A_c & B_c \\ C_c & 0 \end{bmatrix}: \quad A_c = A - BL - KC + KDL, \quad B_c = K, \quad C_c = L
```
This is typically a harder problem since the search space contains non-stabilizing controllers, and the set of stabilizing gains is non-convex. (For state feedback, a nice result exists that says that there are no local minima, but the space of stabilizing gains is still non-convex.)



"""

# ╔═╡ f8142e15-0677-4df1-bd5e-5ba8f54d4cf5
function systems_sf(params::AbstractVector{T}, P) where T
    n2 = length(params) ÷ 2
    L = params[1:n2]'
    K = params[n2+1:2n2, 1:1]
    C = observer_controller(P, L, K)
    G = extended_gangoffour(P, C) # [S PS; CS T]
    C, G
end

