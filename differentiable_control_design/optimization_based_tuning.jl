function expect(name, C, val)
	isdefined(@__MODULE__, name) || return var_not_defined(name)
	if C === nothing
		still_nothing()
	elseif !isa(C, typeof(val))
		wrong_type(name, Matrix)
	elseif !isapprox(C, val, rtol=1e-5)
		almost("$name is not quite correct")
	else
		correct()
	end
end

